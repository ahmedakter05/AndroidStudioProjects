package com.aa.taseen.sdpdemo1.Models;

public class Staff {
    private String name, age;

    public Staff() {

    }
    public Staff(String name, String age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }
}
